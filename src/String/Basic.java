package String;

public class Basic {
    String s1 = new String("Java");   // using new keyword
    String s2 = "Java"; // using literal
    String s3 = "Java";

    public static void main(String[] args) {
        Basic bs = new Basic();
        System.out.println(bs.s1);
        System.out.println(bs.s2);
        System.out.println(bs.s3);

    }
}
