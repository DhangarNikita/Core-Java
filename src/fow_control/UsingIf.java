package fow_control;
/*
If the number is positive, print "The number is positive.
If the number is negative, print "The number is negative."
 */
public class UsingIf {
    public static void main(String[] args) {
        int a=-1;
        if(a>=0){
            System.out.println("the number is positive " + a);
        }else{
            System.out.println("number is negative "+ a);
        }
    }
}
