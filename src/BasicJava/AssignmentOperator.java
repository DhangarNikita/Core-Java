package BasicJava;

public class AssignmentOperator {
    public static void main(String[]args){
        // use assignment operator
        int a=10;
        System.out.println(a+=5) ; // same as a=a+5 =15
        System.out.println(a-=10); // same as a=a-10 = 5
        System.out.println(a*=5); // same as a=a*5 = 25
        System.out.println(a/=5); // same as a= a/5 =5
        System.out.println(a%=5); // same as a= a% 5 =0



    }
}
